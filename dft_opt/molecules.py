from pyscfad import gto


def get_molecule(name, basis):
    mol = gto.Mole()
    mol.basis = basis
    mol.verbose = 0
    mol.unit = "Bohr"

    spin = 0
    if name == "O2":
        structure = "O -1 0 0; O 1 0 0"
        spin = 2
    elif name == "NO":
        structure = "N -1 0 0; O 1 0 0"
        spin = 1
    else:
        structure = PYSCF_MAP[name]

    mol.atom = structure
    mol.spin = spin
    mol.build(trace_coords=True, trace_exp=True, trace_ctr_coeff=True)  
    return mol


